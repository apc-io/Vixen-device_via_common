script_dir=$(dirname $0)

tmp=`getprop ro.boot.console`
if [ $tmp = "ttyGS0" ]; then
   rm -rf /dev/console
   ln -s /dev/null /dev/console
fi

for i in $script_dir/S??*
do
    # Ignore dangling symlinks for now.
    [ ! -f "$i" ] && continue
    echo "[Script] start to exec $i ..."	> /dev/console
    ./$i 2>/dev/console 1>/dev/console
done
